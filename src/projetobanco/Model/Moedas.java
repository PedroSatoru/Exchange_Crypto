package projetobanco.Model;

public class Moedas {

    private double valor;
    private String nome;

    public Moedas(double valor, String nome) {
        this.valor = valor;
        this.nome = nome;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getNome() {
        return nome;
    }
}
